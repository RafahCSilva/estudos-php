<?php

declare(strict_types=1);

namespace RCS\DesignPatterns1\Pt3Estruturais\FlyWeight;

class Soldier
{
    private string $name;
    private int $age;

    public function __construct($name, $age)
    {
        $this->name = $name;
        $this->age = $age;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function getAge(): int
    {
        return $this->age;
    }
}
